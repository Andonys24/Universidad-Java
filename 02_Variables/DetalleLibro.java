public class DetalleLibro {
    public static void main(String[] args) {
        // Detalle de un libro
        String tituloLibro = "El senior de los anillos";
        String autor = "J.R.R. Tolkien";
        int anioPublicacion = 1954;
        double precio = 19.99;
        boolean esBestSeller = true;

        // Imprimir el valor de las variables
        System.out.println("Título: " + tituloLibro);
        System.out.println("Autor: " + autor);
        System.out.println("Año de publicación: " + anioPublicacion);
        System.out.println("Precio: " + precio);
        System.out.println("¿Es best seller?: " + esBestSeller);
    }
}
